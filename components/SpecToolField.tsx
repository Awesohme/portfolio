function Slot({ slotIndex }: { slotIndex: number }) {
  const [idx, setIdx] = useState(slotIndex % TOOLS.length);
  const [shown, setShown] = useState(false);
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const reduce = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    // stagger first appearance so they don't all pop in together;
    // slot 1 (index 3) starts hidden so only ~3 are visible at the start.
    const startDelay = 300 + slotIndex * 1700;
    const t0 = setTimeout(() => setShown(true), slotIndex === 3 ? 6000 : startDelay);
    if (reduce) return () => clearTimeout(t0);

    // each slot has a distinct period + long hidden gap, so at any moment
    // roughly 2-3 of the 4 are visible while the others are faded out.
    const period = 8500 + slotIndex * 1900;
    const hiddenGap = 2600;
    const id = setInterval(() => {
      setShown(false);
      setTimeout(() => {
        setIdx((p) => (p + SLOTS.length) % TOOLS.length);
        setShown(true);
      }, hiddenGap);
    }, period);
    return () => {
      clearTimeout(t0);
      clearInterval(id);
    };
  }, [slotIndex]);

  const s = SLOTS[slotIndex];
  const tool = TOOLS[idx];
  return (
    <div
      ref={ref}
      className={`stf-slot ${shown ? "in" : ""}`}
      style={{
        top: s.top,
        left: s.left,
        ["--depth" as string]: s.depth,
        ["--rot" as string]: `${s.rot}deg`,
      }}
    >
      <div className="stf-tile">
        <span className="stf-glyph">{tool.glyph}</span>
        <span className="stf-label">{tool.label}</span>
      </div>
    </div>
  );
}

export default function SpecToolField() {
  const layerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const reduce = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    if (reduce) return;
    let raf = 0;
    const onMove = (e: MouseEvent) => {
      const mx = e.clientX / window.innerWidth - 0.5;
      const my = e.clientY / window.innerHeight - 0.5;
      cancelAnimationFrame(raf);
      raf = requestAnimationFrame(() => {
        const layer = layerRef.current;
        if (!layer) return;
        layer.querySelectorAll<HTMLElement>(".stf-slot").forEach((node) => {
          const d = parseFloat(node.style.getPropertyValue("--depth") || "1");
          node.style.setProperty("--px", `${-mx * 28 * d}px`);
          node.style.setProperty("--py", `${-my * 28 * d}px`);
        });
      });
    };
    window.addEventListener("mousemove", onMove);
    return () => {
      window.removeEventListener("mousemove", onMove);
      cancelAnimationFrame(raf);
    };
  }, []);

  return (
    <div ref={layerRef} className="spec-toolfield" aria-hidden="true">
      {SLOTS.map((_, i) => (
        <Slot key={i} slotIndex={i} />
      ))}
    </div>
  );
}
